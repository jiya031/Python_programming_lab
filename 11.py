# Create dictionary
my_dict = {"name": "Alice", "age": 22, "city": "Delhi"}

# 1) Print dictionary items
print("Dictionary items:", my_dict.items())

# 2) Access items
print("Name:", my_dict["name"])

# 3) use get()
print("Age:", my_dict.get("age"))

# 4) Change values
my_dict["city"] = "Mumbai"
print("Updated dictionary:", my_dict)

# 5) use len()
print("Length of dictionary:", len(my_dict))
